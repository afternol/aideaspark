async function main() {
  const pool = new Pool({ connectionString: process.env.DATABASE_URL });
  let updated = 0;

  for (const [number, data] of Object.entries(INSIGHTS)) {
    const result = await pool.query(
      'UPDATE "Idea" SET "whyNow" = $1, "noveltyNote" = $2, "strengthNote" = $3 WHERE number = $4',
      [data.whyNow, data.noveltyNote, data.strengthNote, parseInt(number)]
    );
    if (result.rowCount > 0) updated++;
    process.stdout.write(`#${number} ✓  `);
  }

  console.log(`\n✅ ${updated}件更新完了`);
  await pool.end();
}

main().catch(console.error);
